package p1;
public class sameclass{
	private int a=10;
	int b=20;
	protected int c=30;
	public int d=40;
	public sameclass()
	{
		System.out.println("a value form sameclass: "+a);
		System.out.println("b value form sameclass: "+b);
		System.out.println("c value form sameclass: "+c);
		System.out.println("d value form sameclass: "+d);

	}
}